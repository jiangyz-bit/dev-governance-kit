import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const serverDir = process.env.SERVER_REPO_DIR
  ? path.resolve(process.env.SERVER_REPO_DIR)
  : path.resolve(root, "..", "{{SERVER_REPO_DIR_NAME}}");
const sourcePath = path.join(serverDir, "docs", "status-enums.json");
const outputPath = path.join(root, "docs", "STATUS_ENUM_REGISTRY.md");

const registry = JSON.parse(fs.readFileSync(sourcePath, "utf8"));

function renderGroup(group) {
  const hasNext = group.values.some((value) => Array.isArray(value.next));
  const lines = [`## ${group.name}`, "", `Type: \`${group.type}\``, ""];
  if (hasNext) {
    lines.push("| code | desc | allowed next codes |", "|---|---|---|");
    for (const value of group.values) {
      const next = value.next?.length ? value.next.map((code) => `\`${code}\``).join(", ") : "";
      lines.push(`| \`${value.code}\` | ${value.desc} | ${next} |`);
    }
  } else {
    lines.push("| code | desc |", "|---|---|");
    for (const value of group.values) lines.push(`| \`${value.code}\` | ${value.desc} |`);
  }
  return lines.join("\n");
}

const markdown = [
  "# Status Enum Registry",
  "",
  "> Generated from the server repository `docs/status-enums.json`. Do not edit this file manually.",
  "",
  "The backend is the only status enum source of truth. This repository keeps a generated mirror for agent guidance and review.",
  "",
  ...registry.groups.map(renderGroup),
  "",
].join("\n");

fs.writeFileSync(outputPath, markdown, "utf8");
console.log(`Generated ${path.relative(root, outputPath)}`);

