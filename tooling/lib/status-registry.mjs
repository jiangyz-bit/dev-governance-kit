import { GovernanceError } from "./errors.mjs";

export function validateStatusSource(source) {
  const groupNames = new Set();
  for (const group of source.groups ?? []) {
    if (groupNames.has(group.name)) {
      throw new GovernanceError(
        "DUPLICATE_STATUS_GROUP",
        `状态组重复：${group.name}`
      );
    }
    groupNames.add(group.name);

    const codes = new Set();
    for (const value of group.values ?? []) {
      if (codes.has(value.code)) {
        throw new GovernanceError(
          "DUPLICATE_STATUS_CODE",
          `状态组 ${group.name} 中 code 重复：${value.code}`,
          { group: group.name, code: value.code }
        );
      }
      codes.add(value.code);
    }
    for (const value of group.values ?? []) {
      for (const next of value.next ?? []) {
        if (!codes.has(next)) {
          throw new GovernanceError(
            "UNKNOWN_NEXT_STATUS",
            `${group.name}.${value.code} 包含未知后继状态：${next}`,
            { group: group.name, code: value.code, next }
          );
        }
      }
    }
  }
}

function renderGroup(group) {
  const hasNext = group.values.some((value) => Array.isArray(value.next));
  const lines = [`## ${group.name}`, "", `Type: \`${group.type}\``, ""];
  if (hasNext) {
    lines.push("| code | desc | allowed next codes |", "|---|---|---|");
    for (const value of group.values) {
      const next = value.next?.length
        ? value.next.map((code) => `\`${code}\``).join(", ")
        : "";
      lines.push(`| \`${value.code}\` | ${value.desc} | ${next} |`);
    }
  } else {
    lines.push("| code | desc |", "|---|---|");
    for (const value of group.values) {
      lines.push(`| \`${value.code}\` | ${value.desc} |`);
    }
  }
  return lines.join("\n");
}

export function renderStatusRegistry(source, { remote = false } = {}) {
  validateStatusSource(source);
  const introduction = remote
    ? [
        "> Generated from the server repository `docs/status-enums.json`. Do not edit this file manually.",
        "",
        "The backend is the only status enum source of truth. This repository keeps a generated mirror for agent guidance and review."
      ]
    : [
        "> Generated from `docs/status-enums.json`. Do not edit this file manually.",
        "",
        "Databases store `code`; APIs may return `code` and optional `desc`; pages display `desc`.",
        "",
        "## Rules",
        "",
        "- Register every business status in `docs/status-enums.json` before using it in code, SQL, or UI.",
        "- Do not use display descriptions as persisted values.",
        "- Do not create ad hoc status strings inside controllers, mappers, pages, or tests.",
        "- Backend services enforce transitions. Frontend workflow helpers must mirror backend behavior."
      ];

  return [
    "# Status Enum Registry",
    "",
    ...introduction,
    "",
    ...source.groups.map(renderGroup),
    ""
  ].join("\n");
}
